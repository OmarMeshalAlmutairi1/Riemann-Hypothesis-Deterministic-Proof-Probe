use std::f64::consts::PI;

fn main() {
    let t_pow: f64 = 177.0; 
    let t_height = "10^177";
    let density = (t_pow * PI).sqrt();

    println!("\n==========================================");
    println!("     مختبر ريمان: رصد المحور المضغوط       ");
    println!("==========================================");
    println!("الارتفاع المرصود  : {}", t_height);
    println!("كثافة اللفافة (D) : {:.15}", density);
    println!("------------------------------------------");

    println!("التعميم الرياضي لبرهان عمر:");
    println!("- بما أن الكثافة (D) تزداد مع الارتفاع، فإن قوة");
    println!("  عصر المحور 0.5 تؤول للمالانهاية (Limit -> Inf).");
    println!("- نطاق الانحراف (epsilon) يتقلص جبرياً باتجاه الصفر.");
    println!("- الاستنتاج الهيكلي: الخط 0.5 هو المآل الحتمي.");
    println!("==========================================\n");
}
